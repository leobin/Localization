    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateActionPerformed
        if (this.isEdit) {
            datatype.setDataTypeName(inputName.getText());
            datatype.setDataTypeDescription(inputDescription.getText());
            datatype.setDataTypeClassName((String) inputClass.getSelectedItem());
            DataTypeDataManagement.updateDataType(datatype);
        } else {
            datatype = new DataTypeDataObject();
            datatype.setDataTypeName(inputName.getText());
            datatype.setDataTypeDescription(inputDescription.getText());
            datatype.setDataTypeClassName((String) inputClass.getSelectedItem());
            datatype.setDataTypeId(DataTypeDataManagement.addNewDataType(datatype));
        }
        this.parent.dispose();
        CardLayoutHandlerFunction.changeCardView(LocalizationMain.PANEL_BROWSE_DATATYPE);
    }//GEN-LAST:event_buttonCreateActionPerformed

