    private void initOtherComponents() throws IOException, ClassNotFoundException {
        //init list datatype class
//        Class[] className = PackageReader.getclassName("com.localization.other");
        String[] className = PackageReader.getClassName(ApplicationConfiguration.load().datatypePackage);
        DefaultComboBoxModel defaultComboBoxModel = new DefaultComboBoxModel(className);
        inputClass.setModel(defaultComboBoxModel);

        if (this.datatype != null) {
            inputName.setText(datatype.getDataTypeName());
            inputDescription.setText(datatype.getDataTypeDescription());

            //init selected class for datatype
            int selectedClassIndex = 0;
            String selectedClassName = datatype.getDataTypeClassName();
            while (selectedClassIndex < className.length) {
                if (selectedClassName.equals(className[selectedClassIndex])) {
                    break;
                }
                selectedClassIndex++;
            }
            if (selectedClassIndex < className.length) {
                inputClass.setSelectedIndex(selectedClassIndex);
            }

            this.isEdit = true;
            buttonCreate.setText("Edit");
        }
    }
