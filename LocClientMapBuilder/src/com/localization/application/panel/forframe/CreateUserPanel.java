    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateActionPerformed
        if (this.isEdit) {
            user.setUserName(inputUsername.getText());
            user.setPassword(inputPassword.getText());
            user.setUserType(inputType.getSelectedIndex());
            UserDataManagement.updateUser(user);
        } else {
            user = new UserDataObject();
            user.setUserName(inputUsername.getText());
            user.setPassword(inputPassword.getText());
            user.setUserType(inputType.getSelectedIndex());
            user.setUserId(UserDataManagement.addNewUser(user));
        }
        this.parent.dispose();
        CardLayoutHandlerFunction.changeCardView(LocalizationMain.PANEL_BROWSE_USER);
    }//GEN-LAST:event_buttonCreateActionPerformed

